async def seed_database(session: AsyncSession) -> int:
    """Очищает старые вакансии и заливает актуальные из кода."""
    await session.execute(delete(Offer))

    count = 0
    for offer_data in build_offers():
        session.add(offer_data)
        count += 1

    await session.flush()
    return count
